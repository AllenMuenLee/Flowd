def display_board(board):
    """
    Display the Tic Tac Toe board.
    """
    print("+-------+-------+-------+")
    print(f"|       |       |       |")
    print(f"|   {board[0]}   |   {board[1]}   |   {board[2]}   |")
    print(f"|       |       |       |")
    print("+-------+-------+-------+")
    print(f"|       |       |       |")
    print(f"|   {board[3]}   |   {board[4]}   |   {board[5]}   |")
    print(f"|       |       |       |")
    print("+-------+-------+-------+")
    print(f"|       |       |       |")
    print(f"|   {board[6]}   |   {board[7]}   |   {board[8]}   |")
    print(f"|       |       |       |")
    print("+-------+-------+-------+")